section .data

global _main
section .text
_main:
	PUSH RBP
	MOV RBP, RSP
.BEGIN:
.L1:
	JMP .L0
.L0:
	MOV RAX, 3
	PUSH RAX
	MOV RAX, 2
	PUSH RAX
	MOV RAX, 255
	POP RCX
	CDQ
	IDIV RCX
	PUSH RAX
	MOV RAX, 2
	PUSH RAX
	MOV RAX, 255
	POP RCX
	CDQ
	IDIV RCX
	POP RCX
	ADD RAX, RCX
	POP RCX
	ADD RAX, RCX
	PUSH RAX
	POP RDI
	CALL _get_integer
	JMP .END
.END:
	LEAVE
	RET
_get_integer:
	PUSH RBP
	MOV RBP, RSP
	SUB RSP, 16; 4 alloc, 12 padding
.BEGIN:
	MOV [RBP-4], EDI
.L1:
	JMP .L0
.L0:
	MOV EAX, DWORD [RBP-4]; load
	JMP .END
.END:
	ADD RSP, 16; 4 alloc, 12 padding
	LEAVE
	RET
